n = eval(input())

result = 0

if 0 <= n <= 540000:
    result = n * 0.05
elif 540001 <= n <= 1210000:
    result = 540000 * 0.05 + (n - 540000) * 0.12
elif 1210001 <= n <= 2420000:
    result = 540000 * 0.05 + (1210000 - 540000) * 0.12 + (n - 1210000) * 0.2
elif 2420001 <= n <= 4530000:
    result = 540000 * 0.05 + (1210000 - 540000) * 0.12 + (2420000 - 1210000) * 0.2 + (n - 2420000) * 0.3
else:
    result = 540000 * 0.05 + (1210000 - 540000) * 0.12 + (2420000 - 1210000) * 0.2 + (4530000 - 2420000) * 0.3 + (n - 4530000) * 0.4

print(round(result, 1))