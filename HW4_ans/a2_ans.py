n = eval(input("請輸入綜合所得淨額："))

result = 0

if 0 <= n <= 520000:
    result = n * 0.05
elif 520001 <= n <= 1170000:
    result = 520000 * 0.05 + (n - 520000) * 0.12
elif 1170001 <= n <= 2350000:
    result = 520000 * 0.05 + (1170000 - 520000) * 0.12 + (n - 1170000) * 0.2
elif 2350001 <= n <= 4400000:
    result = 520000 * 0.05 + (1170000 - 520000) * 0.12 + (2350000 - 1170000) * 0.2 + (n - 2350000) * 0.3
else:
    result = 520000 * 0.05 + (1170000 - 520000) * 0.12 + (2350000 - 1170000) * 0.2 + (4400000 - 2350000) * 0.3 + (n - 4400000) * 0.4

print("綜合所得稅為：", round(result), "元", sep="")