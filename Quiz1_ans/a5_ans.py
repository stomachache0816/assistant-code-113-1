n = eval(input("請輸入綜合所得淨額："))

result = 0

if 0 <= n <= 560000:
    result = n * 0.05
elif 560001 <= n <= 1260000:
    result = 560000 * 0.05 + (n - 560000) * 0.12
elif 1260001 <= n <= 2520000:
    result = 560000 * 0.05 + (1260000 - 560000) * 0.12 + (n - 1260000) * 0.2
elif 2520001 <= n <= 4720000:
    result = 560000 * 0.05 + (1260000 - 560000) * 0.12 + (2520000 - 1260000) * 0.2 + (n - 2520000) * 0.3
else:
    result = 560000 * 0.05 + (1260000 - 560000) * 0.12 + (2520000 - 1260000) * 0.2 + (4720000 - 2520000) * 0.3 + (n - 4720000) * 0.4

print("綜合所得稅為：", round(result), "元", sep="")